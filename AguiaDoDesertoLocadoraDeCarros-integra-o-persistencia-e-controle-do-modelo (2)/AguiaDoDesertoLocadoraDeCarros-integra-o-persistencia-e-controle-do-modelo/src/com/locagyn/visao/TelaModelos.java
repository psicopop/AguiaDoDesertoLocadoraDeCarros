    private void jButtonBuscarModeloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarModeloActionPerformed
        // TODO add your handling code here:
        File workingimages = new File("./src/com/locagyn/imagens");
            
            JFileChooser fc = new JFileChooser();
            fc.setCurrentDirectory(workingimages);
            fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
            fc.showOpenDialog(this);
            File arquivo = fc.getSelectedFile();
            String nomeDoArquivo = arquivo.getPath();
            jTextFieldUrlModelo.setText(nomeDoArquivo);
            ImageIcon iconLogoModelo = new ImageIcon(nomeDoArquivo);
            iconLogoModelo.setImage(iconLogoModelo.getImage().getScaledInstance(
                    jLabelModelo.getWidth(),jLabelModelo.getHeight(),1));
            jLabelModelo.setIcon(iconLogoModelo);
    }//GEN-LAST:event_jButtonBuscarModeloActionPerformed

