    public void ComboboxTxt(){
        try {
            IModeloControle modeloControle = new ModeloControle();
            ArrayList<Modelo> Lista = modeloControle.listagemModelo();
            int tamanho = Lista.size();
            String[] vet = new String[tamanho];
            for (int i = 0 ; i < tamanho; i ++) {
                vet[i] = Lista.get(i).getDescricaoModelo();
            jComboBoxModelo.addItem(vet[i]);
            }
        }catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
        }
        
    }

